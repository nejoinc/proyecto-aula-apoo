class ContentProcessor:
    @staticmethod
    def process_audio(file):
        """
        Simula la transcripción de audio.
        Más adelante puedes usar SpeechRecognition o una API de IA.
        """
        return f"Transcripción simulada del archivo de audio {file}"

    @staticmethod
    def clean_text(text):
        """
        Simula limpieza del texto (quitar saltos, espacios, caracteres raros).
        """
        return text.strip()
